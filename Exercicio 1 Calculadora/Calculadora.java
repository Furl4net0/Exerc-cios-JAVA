public class Calculadora {

    static float soma(float a, float b){
        return a + b;

    }

    static float subtracao(float a, float b){
        return a - b;
    }

    static public float divisao(float a, float b){
        return a / b;
    }

    static float multiplicacao(float a, float b){
        return a * b;
    }
}
